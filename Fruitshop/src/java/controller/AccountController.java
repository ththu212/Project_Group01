    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("infomation.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        String action = request.getParameter("action");
        if (action != null) {
            if (action.equals("Update")) {
                String user = request.getParameter("username");
                String fullname = request.getParameter("fullname");
                String phone = request.getParameter("phone");
                String mail = request.getParameter("mail");
                String address = request.getParameter("address");
                Users u = new Users();
                boolean rs = u.updateAcc(user, fullname, mail, phone, address);
                if (rs) {
                    Users info = u.getInfoAcount(user);
                    session.setAttribute("info", info);
                    request.setAttribute("thongbao", "Cập nhật thành công!");
                    request.getRequestDispatcher("infomation.jsp").forward(request, response);
                } else {
                    response.sendRedirect("404.jsp");
                }
            }
        }
        
    }

